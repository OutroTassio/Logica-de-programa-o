###
# Fazer um programa que leia três medidas b, B e h. Em seguida, calcular e mostrar (Com quatro casas decimais.
# ###


b = float(input('Valor da base menor: '))
B = float(input('Valor da base menor: '))
h = float(input('Valor da altura: '))

area = (b + B) / 2 * h

print(f'A área do trapézio é {area:.4f}')

